# AND (and): Retorna True se ambas as expressões são verdadeiras.
# OR (or): Retorna True se pelo menos uma das expressões é verdadeira.
# NOT (not): Inverte o valor da expressão booleana.

p = True
q = False

resultado_and = p and q
resultado_or = p or q
resultado_not = not p

print(resultado_and, resultado_or, resultado_not)
