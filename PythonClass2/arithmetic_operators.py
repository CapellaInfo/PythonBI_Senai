# Adição (+): Soma dois valores.
# Subtração (-): Subtrai o valor à direita do valor à esquerda.
# Multiplicação (*): Multiplica dois valores.
# Divisão (/): Divide o valor à esquerda pelo valor à direita.
# Divisão Inteira (//): Divide, mas retorna apenas a parte inteira do resultado.
# Módulo (%): Retorna o resto da divisão entre os dois valores.
# Exponenciação ():** Eleva o valor à esquerda à potência do valor à direita.

a = 10
b = 3

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divisao_inteira = a // b
resto = a % b
potencia = a ** b

print(soma, subtracao, multiplicacao, divisao, divisao_inteira, resto, potencia)
