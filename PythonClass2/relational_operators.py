# Igual (==): Retorna True se os valores são iguais.
# Diferente (!=): Retorna True se os valores são diferentes.
# Menor (<): Retorna True se o valor à esquerda é estritamente menor que o valor à direita.
# Maior (>): Retorna True se o valor à esquerda é estritamente maior que o valor à direita.
# Menor ou Igual (<=): Retorna True se o valor à esquerda é menor ou igual ao valor à direita.
# Maior ou Igual (>=): Retorna True se o valor à esquerda é maior ou igual ao valor à direita.

x = 5
y = 10

igual = x == y
diferente = x != y
menor = x < y
maior = x > y
menor_igual = x <= y
maior_igual = x >= y

print(igual, diferente, menor, maior, menor_igual, maior_igual)
